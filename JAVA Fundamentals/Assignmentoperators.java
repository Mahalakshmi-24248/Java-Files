class Assignmentoperators {
      public static void main(String[] args) {
	int a=10 , b=20;
	//using=
	System.out.println("a=b"+ a=b);
	//using+=
	System.out.println("a+=b"+a+=b);
	//using-=
	System.out.println("a-=b"+ a-=b);
	//using*=
	System.out.println("a*=b"+ a*=b);
	//using/=
	System.out.println("a/=b"+ a/=b);
	using%=
	System.out.println("a%=b"+ a%=b);
 }
}