class Arithmeticoperators {
  public static void main(String[] args) {
	int a=15,b=20;
	//Addition operator
	System.out.println("a + b ="+(a+b));
	//Subraction operator
	System.out.println("a - b ="+(a-b));
	//Multiplication operatior
	System.out.println("a * b ="+(a*b));
	//Division operator
	System.out.println("a / b ="+(a/b));
	//Modulo operator
	System.out.print("a % b ="+(a%b));
  }
}